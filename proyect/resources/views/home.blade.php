    <h1>Recursos Naturales</h1>
    <p>Los recursos naturales son elementos o materiales que se encuentran en la naturaleza y que los seres humanos utilizan para satisfacer sus necesidades y mejorar su calidad de vida. Estos recursos pueden ser renovables o no renovables.</p>
    <h2>Renovables:</h2>
    <ul>
        <li><strong>Agua dulce:</strong> Para el consumo humano, riego agrícola, generación de energía hidroeléctrica, entre otros.<br><img class="imagen" src="img/agua.jpg" alt="Agua Dulce"></li>
        <li><strong>Energía solar:</strong> Utilizada para generar electricidad a través de paneles solares.<br><img class="imagen" src="img/solar.jpg" alt="Energía Solar"></li>
        <li><strong>Energía eólica:</strong> Generada a partir del viento y convertida en electricidad mediante turbinas eólicas.<br><img class="imagen" src="img/aire.jpg" alt="Energía Eólica"></li>
        <li><strong>Biomasa:</strong> Materia orgánica utilizada como fuente de energía, como la madera, el estiércol, etc.<br><img class="imagen" src="img/biomasa.jpg" alt="Biomasa"></li>
    </ul>
    <h2>No renovables:</h2>
    <ul>
        <li><strong>Petróleo y gas natural:</strong> Utilizados como combustibles y en la producción de una amplia variedad de productos químicos.<br><img class="imagen" src="img/petroleo.jpg" alt="Petróleo y Gas Natural"></li>
        <li><strong>Carbón:</strong> Utilizado principalmente para la generación de electricidad y en la industria siderúrgica.<br><img class="imagen" src="img/carbon.jpg" alt="Carbón"></li>
        <li><strong>Minerales:</strong> Como el hierro, cobre, oro, plata, utilizados en la fabricación de productos industriales.<br><img class="imagen" src="img/minerales.jpg" alt="Minerales"></li>
        <li><strong>Uranio:</strong> Utilizado como combustible en plantas de energía nuclear para la generación de electricidad.<br><img class="imagen" src="img/uranio.jpg" alt="Uranio"></li>
    </ul>
    <p>La gestión sostenible de los recursos naturales es fundamental para garantizar su disponibilidad para las generaciones futuras. Esto implica prácticas de conservación, uso eficiente y desarrollo de tecnologías limpias y renovables para reducir la dependencia de los recursos no renovables y mitigar los impactos ambientales asociados con su extracción y uso.</p>

